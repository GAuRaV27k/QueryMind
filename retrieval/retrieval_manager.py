async def retrivel_search(user_query: str) -> dict:
    """
    Plan retrieval, execute provider calls in parallel, and normalize results.
    """
    if not user_query or not user_query.strip():
        raise ValueError("user_query must not be empty")

    planned_retrieval = await plan_retrieval_async(user_query)

    tasks: list = []
    for plan in planned_retrieval:
        for tool in plan.tools:
            if tool == "exa":
                tasks.append(_call_exa(plan.query, plan.intent, plan.priority))
            elif tool == "tavily":
                tasks.append(_call_tavily(plan.query, plan.intent, plan.priority))
            elif tool == "you":
                tasks.append(_call_you(plan.query, plan.intent, plan.priority))
            else:
                raise ValueError(f"Unsupported tool: {tool}")

    if not tasks:
        return {"plans": planned_retrieval, "results": [], "failures": []}

    bundles = await asyncio.gather(*tasks)

    results: list[UnifiedRetrievalResult] = []
    failures: list[dict] = []
    for bundle in bundles:
        if bundle.get("status") != "success":
            failures.append(bundle)
            continue
        results.extend(normalize_provider_bundle(bundle))

    return {"plans": planned_retrieval, "results": results, "failures": failures}




