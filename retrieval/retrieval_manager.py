async def _call_exa(
    query: str,
    intent: str,
    priority: str,
):
    """
    Execute an Exa search based on RetrievalPlan metadata.

    Args:
        query: Expanded query generated by planner.
        intent: Planner classified intent.
        priority: high | medium | low

    Returns:
        Provider response bundle.
    """

    if not query:
        raise ValueError("query must not be empty")

    intent = (intent or "research").lower()
    max_results = _get_max_results(priority)

    contents = {"highlights": True}
    if intent in {"research", "technical_comparison", "coding"}:
        contents["text"] = True
    if intent in {"research", "technical_comparison"}:
        contents["summary"] = True

    kwargs = {
        "type": "auto",
        "num_results": max_results,
        "contents": contents,
    }

    search_call = exa_client.search

    try:
        if asyncio.iscoroutinefunction(search_call):
            response = await search_call(query, **kwargs)
        else:
            response = await asyncio.to_thread(
                search_call,
                query,
                **kwargs,
            )
    except Exception as e:
        return {
            "provider": "exa",
            "query": query,
            "intent": intent,
            "priority": priority,
            "status": "failed",
            "error": str(e),
            "response": None,
        }

    return {
        "provider": "exa",
        "query": query,
        "intent": intent,
        "priority": priority,
        "status": "success",
        "response": response,
    }
