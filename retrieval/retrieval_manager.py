def _get_max_results(priority: str) -> int:
    """
    Determine retrieval breadth based on planner priority.
    """
    priority = priority.lower()

    if priority == "high":
        return 8

    if priority == "medium":
        return 5

    return 3


async def _call_tavily(
    query: str,
    intent: str,
    priority: str,
):
    """
    Execute a Tavily search based on RetrievalPlan metadata.

    Args:
        query: Expanded query generated by planner.
        intent: Planner classified intent.
        priority: high | medium | low

    Returns:
        Provider response bundle.
    """

    if not query:
        raise ValueError("query must not be empty")

    intent = (intent or "factoid").lower()

    # -----------------------------
    # Retrieval Strategy
    # -----------------------------

    topic = "general"
    search_depth = "advanced"
    include_raw_content = False

    if intent == "freshness":
        topic = "news"
        search_depth = "advanced"

    elif intent == "factoid":
        topic = "general"
        search_depth = "basic"

    elif intent == "research":
        topic = "general"
        search_depth = "advanced"
        include_raw_content = True

    elif intent == "technical_comparison":
        topic = "general"
        search_depth = "advanced"
        include_raw_content = True

    max_results = _get_max_results(priority)

    # -----------------------------
    # Execute Search
    # -----------------------------

    search_call = tavily_client.search

    kwargs = {
        "query": query,
        "topic": topic,
        "search_depth": search_depth,
        "max_results": max_results,
        "include_favicon": True,
        "include_raw_content": include_raw_content,
    }

    try:

        if asyncio.iscoroutinefunction(search_call):

            response = await search_call(**kwargs)

        else:

            response = await asyncio.to_thread(
                search_call,
                **kwargs,
            )

    except Exception as e:

        return {
            "provider": "tavily",
            "query": query,
            "intent": intent,
            "priority": priority,
            "status": "failed",
            "error": str(e),
            "response": None,
        }

    # -----------------------------
    # Return Provider Bundle
    # -----------------------------

    return {
        "provider": "tavily",
        "query": query,
        "intent": intent,
        "priority": priority,
        "status": "success",
        "response": response,
    }
